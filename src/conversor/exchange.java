    // Método para obtener los símbolos de las tasas de cambio de la API de apilayer y agregarlos a los ComboBox divisa_1 y divisa_2
    private void agregarSimbolos() {
        String simbolosJSON = ApiService.obtenerSimbolos();

        try {
            JSONObject jsonObject = new JSONObject(simbolosJSON);
            JSONObject symbols = jsonObject.getJSONObject("symbols");
            Iterator<String> keys = symbols.keys();
            ArrayList<String> simbolosList = new ArrayList<String>();
            while (keys.hasNext()) {
                String key = keys.next();
                String value = symbols.getString(key);
                String simbolo = key + ": " + value;
                simbolosList.add(simbolo);
            }

            // Ordenar la lista de símbolos alfabéticamente
            Collections.sort(simbolosList);

            String[] simbolosArray = simbolosList.toArray(new String[simbolosList.size()]);
            DefaultComboBoxModel<String> model1 = new DefaultComboBoxModel<>(simbolosArray);
            divisa_1.setModel(model1);

            String[] simbolosArray2 = simbolosList.toArray(new String[simbolosList.size()]);
            DefaultComboBoxModel<String> model2 = new DefaultComboBoxModel<>(simbolosArray2);
            divisa_2.setModel(model2);

        } catch (JSONException e) {
            System.out.println("Error parsing JSON: " + e.getMessage());
        }
    }

