    private void convertirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_convertirActionPerformed
        // TODO add your handling code here:
        String divisa1 = "";
        String divisa2 = "";
        double cantidad = 0.0;

        // Verificar que se hayan seleccionado valores en los ComboBox
        if (divisa_1.getSelectedIndex() != -1 && divisa_2.getSelectedIndex() != -1) {
            // Obtener las abreviaturas de las divisas seleccionadas
            String divisa1Seleccionada = divisa_1.getSelectedItem().toString();
            String divisa2Seleccionada = divisa_2.getSelectedItem().toString();
            if (divisa1Seleccionada.length() >= 3 && divisa2Seleccionada.length() >= 3) {
                divisa1 = divisa1Seleccionada.substring(0, 3);
                divisa2 = divisa2Seleccionada.substring(0, 3);
            } else {
                System.out.println("Ingresa una divisa válida");
                return;
            }
        } else {
            System.out.println("Selecciona una divisa");
            return;
        }

        // Verificar que se haya ingresado un valor numérico en el campo de texto
        try {
            cantidad = Double.parseDouble(texto_divisa.getText());
        } catch (NumberFormatException e) {
            System.out.println("Ingresa un valor numérico válido");
            return;
        }

        // Llamar a la API de apilayer para obtener el resultado de la conversión
        String resultadoJSON = ApiConvert.convertir(divisa1, divisa2, cantidad);
        try {
            JSONObject resultadoObj = new JSONObject(resultadoJSON);
            double resultado = resultadoObj.getDouble("result");
            /**
             * resultado1.setText(String.format("%.2f %s = %.2f %s", cantidad,
             * divisa1, resultado, divisa2));
             */
            resultado1.setText(String.format("%s", resultado + " " + divisa2));
        } catch (JSONException e) {
            System.out.println("Error parsing JSON: " + e.getMessage());
        }

    }//GEN-LAST:event_convertirActionPerformed

